    public void estadoBotonCargarValidacion() {
        if (existenSeleccionados()) {
            btnCrear.setEnabled(true);
            CargarTab tab = new CargarTab();
            tab.tbUsuarioActivo(jtDetalleValidacion, jtUsuario);
        } else {
            jtDetalleValidacion.removeAll();
            DefaultTableModel modelo = new DefaultTableModel();
            jtDetalleValidacion.setModel(modelo);
            modelo.addColumn("Codigo");
            modelo.addColumn("Nombre");
            modelo.addColumn("ID Custodio");
            modelo.addColumn("Nombre Custodio");
            btnCrear.setEnabled(false);
        }
    }

    public boolean existenSeleccionados() {
        int tamaño = 0;
        int i = 0;
        int resp = 0;
        tamaño = jtUsuario.getRowCount();
        while (i < tamaño) {
            boolean tmp = (boolean) jtUsuario.getValueAt(i, 0);
            if (tmp) {
                resp++;
            }
            i++;
        }
        if (resp > 0) {
            return true;
        }

        return false;
    }

