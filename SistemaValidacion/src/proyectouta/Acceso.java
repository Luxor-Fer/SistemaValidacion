    public Usuario validarUsuario(){
        Consultas_bd cs= new Consultas_bd();
        String msg="";
        if(this.texUsuario.getText().length()==0){
            msg=msg+" *Ingrese un usuario \n";
        }
        if(new String(texPass.getPassword()).length()==0){
            msg=msg+" *Ingrese una contrase침a \n";
        }
        if(this.texUsuario.getText().length()!=0 && new String(texPass.getPassword()).length()!=0){
            
            res=cs.Login(this.texUsuario.getText(), new String(texPass.getPassword()));
            msg=msg+res+"\n";
        }
        msgError=msg;
        return cs.getLogin();
    }
    
    
    
    
    
